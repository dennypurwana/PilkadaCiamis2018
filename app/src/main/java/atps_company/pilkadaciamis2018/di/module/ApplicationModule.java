package atps_company.pilkadaciamis2018.di.module;

import android.app.Application;
import android.content.Context;

import com.google.firebase.database.FirebaseDatabase;

import javax.inject.Singleton;

import atps_company.pilkadaciamis2018.R;
import atps_company.pilkadaciamis2018.di.ApplicationContext;
import dagger.Module;
import dagger.Provides;
import uk.co.chrisjenx.calligraphy.CalligraphyConfig;

/**
 * Created by janisharali on 27/01/17.
 */

@Module
public class ApplicationModule {
    String mBaseUrl;

    public ApplicationModule(String mBaseUrl) {
        this.mBaseUrl = mBaseUrl;
    }

    private  Application mApplication;

    public ApplicationModule(Application application) {
        mApplication = application;
    }

    @Provides
    @ApplicationContext
    Context provideContext() {
        return mApplication;
    }

    @Provides
    Application provideApplication() {
        return mApplication;
    }

    @Provides
    @Singleton
    FirebaseDatabase provideFirebaseDatabase(){
        FirebaseDatabase firebase = FirebaseDatabase.getInstance();
        return firebase;
    }


    @Provides
    @Singleton
    CalligraphyConfig provideCalligraphyDefaultConfig() {
        return new CalligraphyConfig.Builder()
                .setDefaultFontPath("SourceSansPro-Regular.ttf")
                .setFontAttrId(R.attr.fontPath)
                .build();
    }

}
